from __future__ import annotations

import unittest
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[1]
TRANSLATION_SKILL = REPO_ROOT / ".claude/skills/update-feature-metadata-translations/SKILL.md"
PUBLISH_SKILL = REPO_ROOT / ".claude/skills/publish-shared-dataset/SKILL.md"
AGENTS = REPO_ROOT / "AGENTS.md"
SKILLS_README = REPO_ROOT / ".claude/skills/README.md"


class AgentTranslationSkillsTests(unittest.TestCase):
    def test_translation_skill_documents_source_generated_contract(self):
        skill = TRANSLATION_SKILL.read_text(encoding="utf-8")

        self.assertIn("name: update-feature-metadata-translations", skill)
        self.assertIn("Which locales should be autogenerated by the agent during first upload?", skill)
        self.assertIn("Which metadata fields should be autogenerated by the agent during first upload?", skill)
        self.assertIn("{asset-slug}.metadata-translations.csv", skill)
        self.assertIn("feature_id,field,locale,source_value_hash,value,review_state,notes", skill)
        self.assertIn("scripts/feature_metadata_localization.py", skill)
        self.assertIn("--all-locales", skill)
        self.assertIn("Do not add frontend merge logic or a translation overlay fetch", skill)

    def test_repo_skill_indexes_include_translation_skill(self):
        agents = AGENTS.read_text(encoding="utf-8")
        skills_readme = SKILLS_README.read_text(encoding="utf-8")

        self.assertIn(".claude/skills/update-feature-metadata-translations/SKILL.md", agents)
        self.assertIn("Use `update-feature-metadata-translations`", agents)
        self.assertIn("Release feature metadata translations", agents)
        self.assertIn("update-feature-metadata-translations", skills_readme)

    def test_publish_skill_requires_first_upload_translation_questions(self):
        skill = PUBLISH_SKILL.read_text(encoding="utf-8")
        normalized_skill = " ".join(skill.split())

        self.assertIn(".claude/skills/update-feature-metadata-translations/SKILL.md", skill)
        self.assertIn("Which locales should be autogenerated by the agent during first upload?", skill)
        self.assertIn("Which metadata fields should be autogenerated by the agent during first upload?", skill)
        self.assertIn("{asset-slug}.metadata-translations.csv", skill)
        self.assertIn("scripts/feature_metadata_localization.py --all-locales", skill)
        self.assertIn("generated localized metadata sidecars", normalized_skill)


if __name__ == "__main__":
    unittest.main()
