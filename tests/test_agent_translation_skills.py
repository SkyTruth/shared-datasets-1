from __future__ import annotations

import unittest
from pathlib import Path


REPO_ROOT = Path(__file__).resolve().parents[1]
TRANSLATION_SKILL = REPO_ROOT / ".claude/skills/update-feature-metadata-translations/SKILL.md"
PUBLISH_SKILL = REPO_ROOT / ".claude/skills/publish-shared-dataset/SKILL.md"
AGENTS = REPO_ROOT / "AGENTS.md"
SKILLS_README = REPO_ROOT / ".claude/skills/README.md"


class AgentTranslationSkillsTests(unittest.TestCase):
    def test_translation_skill_is_routed_and_linked_from_publish_workflow(self):
        skill = TRANSLATION_SKILL.read_text(encoding="utf-8")
        agents = AGENTS.read_text(encoding="utf-8")
        skills_readme = SKILLS_README.read_text(encoding="utf-8")
        publish_skill = PUBLISH_SKILL.read_text(encoding="utf-8")
        normalized_publish_skill = " ".join(publish_skill.split())

        for marker in (
            "name: update-feature-metadata-translations",
            "Which locales should be autogenerated by the agent during first upload?",
            "Which metadata fields should be autogenerated by the agent during first upload?",
            "{asset-slug}.metadata-translations.csv",
            "feature_id,field,locale,source_value_hash,value,review_state,notes",
            "scripts/feature_metadata_localization.py",
            "scripts/feature_metadata_machine_translate.py",
            "deep-translator",
            "https://translate.google.com/?op=docs",
            "roughly 60,000 rows and about 1 MB each",
            "Do not hard-code WDPA",
            "Do not add frontend merge logic or a translation overlay fetch",
        ):
            with self.subTest(marker=marker):
                self.assertIn(marker, skill)

        self.assertIn(".claude/skills/update-feature-metadata-translations/SKILL.md", agents)
        self.assertIn("Use `update-feature-metadata-translations`", agents)
        self.assertIn("update-feature-metadata-translations", skills_readme)
        self.assertIn(".claude/skills/update-feature-metadata-translations/SKILL.md", publish_skill)
        self.assertIn("scripts/feature_metadata_machine_translate.py", publish_skill)
        self.assertIn("scripts/feature_metadata_localization.py --all-locales", publish_skill)
        self.assertIn("generated localized metadata sidecars", normalized_publish_skill)


if __name__ == "__main__":
    unittest.main()
